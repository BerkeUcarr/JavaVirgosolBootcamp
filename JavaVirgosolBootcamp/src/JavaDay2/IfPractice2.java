package JavaDay2;

public class IfPractice2 {

    public static void main(String[] args) {

        int not = 70;

        if (not >= 0 && not <= 100) {
            System.out.println("Notunuz: " + not);

        } else {
            System.out.println("Hatalı bir değer girdiniz...");
        }
    }
}
